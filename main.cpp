// 128 bits x 64 == 16 byte x 64
BYTE *FrameBuf = 0;

// размер экранчика 16 на 8 символов (символ - это 8 на 8 пикселов)
// пусть младшие 4 бита будут X, а старшие 4 - Y
int pos = 0; // sizeof(int) == 2

void SendCmd(uint8_t Cmd, bool LeftLCD, bool RightLCD);
